x = 5
y = 2

# Addition
puts x+y 
# outputs 7

# Subtraction 
puts x-y 
# outputs 3

# Multiplication
puts x*y
# outputs 10

# Division
puts x/y
# outputs 2

# modulo
puts x%y
# outputs 1

# potencia
puts x**y
# 25

=begin
Cuando divide dos valores enteros , el resultado será un número entero , como se muestra en el ejemplo anterior. Si desea tener un resultado de coma flotante, un operando debe ser un valor de coma flotante: 
x = 5.0 
y = 2 
puts x / y # outputs 2.5
=end

