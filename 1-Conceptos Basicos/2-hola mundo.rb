# Creemos nuestro primer programa Ruby, el clásico programa "Hello World". 
# Para esto, usamos el método de puts incorporado .

puts "Hello World"

# todos los valores de texto (cadenas) deben estar entre comillas simples o dobles .

# Otro método que se puede usar para mostrar la salida a la pantalla es print . 
print "Hello World"

# Este código muestra el mismo resultado que antes, excepto que el puts agrega automáticamente una nueva línea después de la salida, mientras que print no lo hace. 